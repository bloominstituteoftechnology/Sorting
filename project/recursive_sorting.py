    if len(arr) < 2:
        return arr

    c_arr = arr.copy()
    pivot = c_arr[math.floor(len(arr)/2)]
    c_arr.pop(math.floor(len(arr) / 2))
    low = []
    high = []
    for num in c_arr:
        if num < pivot:
            low.append(num)
        else:
            high.append(num)

    return [*quick_sort(low), pivot, *quick_sort(high)]


